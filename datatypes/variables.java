public class variables {
    public static void main(String[] args) {
        //primitive datatype
        // int data types
        int age = 10;  //store the integer using int-keyword range 2billion
        long ages = 10000000000L;  //more than 2B lastly ends with L-
        byte date = 98; //byte keyword storing capacity range of 128 to 127
        short dates = 32000; //contains range of 32000
        System.out.println(date);

        //float datatype
        float marks = 3.14f;   //definitely ends with f or F
        double mark = 4.58947549548d; //long ranged float values
        System.out.println(mark);

        //boolean datatype
        boolean bool = false;
        System.out.println(bool);

        //character datatype
        char c = 'A';   //only single values are stored
        String kjk ="hggfhg";
        System.out.println(kjk);
        System.out.println(c);
    }
}
